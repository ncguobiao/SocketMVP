    /**
     * 将秒转成小时
     *
     * @param value 秒
     * @return
     */
    public static int getHour(Long value) {
        int a = (int) (value % 3600);//取模是否整除 （小时）
        int intTime;
        if (a == 0) {
            intTime = (int) (value / 3600);//计算可以整除下的时间
        } else {
            int l1 = (int) (value / 3600);//获取整除后的时间
            intTime = (l1 + (a / 3600) + 1);//总时间
        }
        return intTime;
    }
