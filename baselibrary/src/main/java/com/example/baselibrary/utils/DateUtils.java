    /**
     * @param second
     * @return
     */
    public static String getTimeFormat(long second) {
        long h = 0;
        long d = 0;
        long s = 0;
//        second = second/1000;
        long temp = second % 3600;
        if (second > 3600) {
            h = second / 3600;
            if (temp != 0) {
                if (temp > 60) {
                    d = temp / 60;
                    if (temp % 60 != 0) {
                        s = temp % 60;
                    }
                } else {
                    s = temp;
                }
            }
        } else {
            d = second / 60;
            if (second % 60 != 0) {
                s = second % 60;
            }
        }

        return h + "时" + d + "分" + s + "秒";
    }

